    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String name = txtmemName.getText();
        String address = txtmemAddress.getText();
        String phone = txtmemPhone.getText();
        
        try {
            pst = con.prepareStatement("insert into member(memName, memAddress, memPhone) value (?,?,?)");
            pst.setString(1, name);
            pst.setString(2, address);
            pst.setString(3, phone);
            
            int k = pst.executeUpdate();
            if (k == 1)
            {
                JOptionPane.showMessageDialog(this, "Đã thêm tác giả mới");
                txtmemName.setText("");
                txtmemAddress.setText("");
                txtmemPhone.setText("");
                txtmemName.requestFocus();
                Member_Load();
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Lỗi");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Member.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        DefaultTableModel d1 = (DefaultTableModel) memTable.getModel();
        int selectIndex = memTable.getSelectedRow();
        
        int id = Integer.parseInt(d1.getValueAt(selectIndex, 0).toString());
        
        String name = txtmemName.getText();
        String address = txtmemAddress.getText();
        String phone = txtmemPhone.getText();
        
        try {
            pst = con.prepareStatement("update member set memName = ?, memAddress = ?, memPhone = ? where memID = ?");
            pst.setString(1, name);
            pst.setString(2, address);
            pst.setString(3, phone);
            pst.setInt(4, id);
            
            int k = pst.executeUpdate();
            if (k == 1){
                JOptionPane.showMessageDialog(this, "Đã cập nhật đọc giả");
                txtmemName.setText("");
                txtmemAddress.setText("");
                txtmemPhone.setText("");
                txtmemName.requestFocus();
                Member_Load();
                btnAdd.setEnabled(true);
            }
            else{
                JOptionPane.showMessageDialog(this, "Lỗi!");
            }
                
        } catch (SQLException ex) {
            Logger.getLogger(Member.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

