    public void Member_Load()
    {
        int c;
        try {
            pst = con.prepareStatement("select * from member");
            rs = pst.executeQuery();
            
            ResultSetMetaData rsd = (ResultSetMetaData) rs.getMetaData();
            c = rsd.getColumnCount();
            
            DefaultTableModel d = (DefaultTableModel)memTable.getModel();
            
            
            d.setRowCount(0);
            
            while (rs.next()) 
            {
                Vector v2 = new Vector();
                for (int i = 1; i <= c; i++) {
                    v2.add(rs.getString("memID"));
                    v2.add(rs.getString("memName"));
                    v2.add(rs.getString("memAddress"));
                    v2.add(rs.getString("memPhone"));
                }
                d.addRow(v2);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Member.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
