    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String category = txtcatName.getText();
        
        try {
            pst = con.prepareStatement("insert into category(catName) value (?)");
            pst.setString(1, category);
            
            int k = pst.executeUpdate();
            if (k == 1)
            {
                JOptionPane.showMessageDialog(this, "Đã thêm danh mục sách mới");
                txtcatName.setText("");
                txtcatName.requestFocus();
                Category_Load();
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Lỗi");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Category.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        DefaultTableModel d1 = (DefaultTableModel) catTable.getModel();
        int selectIndex = catTable.getSelectedRow();
        
        int id = Integer.parseInt(d1.getValueAt(selectIndex, 0).toString());
        
        String category = txtcatName.getText();
        
        try {
            pst = con.prepareStatement("update category set catName = ? where catID = ?");
            pst.setString(1, category);
            pst.setInt(2, id);
            
            int k = pst.executeUpdate();
            if (k == 1){
                JOptionPane.showMessageDialog(this, "Đã cập nhật thành công");
                txtcatName.setText("");
                txtcatName.requestFocus();
                Category_Load();
                btnAdd.setEnabled(true);
            }
            else{
                JOptionPane.showMessageDialog(this, "Lỗi!");
            }
                
        } catch (SQLException ex) {
            Logger.getLogger(Category.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

