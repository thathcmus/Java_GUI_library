    private void txtlendmemIDKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtlendmemIDKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode() == KeyEvent.VK_ENTER)
        {
            String mid = txtlendmemID.getText();
            try {
                pst = con.prepareStatement("select * from member where memID = ?");
                pst.setString(1, mid);
                rs = pst.executeQuery();
                
                if(rs.next() == false)
                {
                    JOptionPane.showMessageDialog(this, "ID thành viên không tìm thấy");
                }
                else
                {
                    String membername = rs.getString("memName");
                    txtlendmemName.setText(membername.trim());
                }
            } catch (SQLException ex) {
                Logger.getLogger(LendBook.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_txtlendmemIDKeyPressed

