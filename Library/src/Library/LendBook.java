    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String mid = txtlendmemID.getText();
        BookItem bitem = (BookItem)txtlendBook.getSelectedItem();
        SimpleDateFormat date_format = new SimpleDateFormat("yyyy-MM-dd");
        String lenddate = date_format.format(txtlendDate.getDate());
        String returndate = date_format.format(txtlendReturn.getDate());
             
        try {
            pst = con.prepareStatement("insert into lendbook(lendmemID, lendbookID, lendDate, lendReturn)values(?,?,?,?)");
            pst.setString(1, mid);
            pst.setInt(2, bitem.id);
            pst.setString(3, lenddate);
            pst.setString(4, returndate);
            int k = pst.executeUpdate();         
            if (k == 1)
            {
                pst = con.prepareStatement("update book set bookStock = bookStock - 1 where bookID = ?");
                pst.setInt(1, bitem.id);
                pst.executeUpdate();
                
                JOptionPane.showMessageDialog(this, "Đã tạo xong lượt mượn sách mới");
                txtlendmemID.setText("");
                txtlendBook.setSelectedIndex(-1);
                txtlendmemName.setText("");
                Lendbook_Load();
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Lỗi!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(LendBook.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAddActionPerformed

