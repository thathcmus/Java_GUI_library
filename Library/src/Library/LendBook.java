    public void Book()
    {
        try {
            pst = con.prepareStatement("select * from book where bookStock > 0");
            rs = pst.executeQuery();
            txtlendBook.removeAllItems();
            
            while(rs.next())
            {
                txtlendBook.addItem(new BookItem(rs.getInt(1), rs.getString(2)));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Book.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    public void Lendbook_Load()
    {
        int c;
        try {
            pst = con.prepareStatement("select l.lendID, m.memName, b.bookName, l.lendDate, l.lendReturn "
                    + "from lendbook l "
                    + "JOIN member m    ON l.lendmemID        = m.memID "
                    + "JOIN book b      ON l.lendbookID       = b.bookID ");
            rs = pst.executeQuery();
            
            ResultSetMetaData rsd = (ResultSetMetaData) rs.getMetaData();
            c = rsd.getColumnCount();
            
            DefaultTableModel d = (DefaultTableModel)lendTable.getModel();
            
            
            d.setRowCount(0);
            
            while (rs.next()) 
            {
                Vector v2 = new Vector();
                for (int i = 1; i <= c; i++) {
                    v2.add(rs.getString("l.lendID"));
                    v2.add(rs.getString("m.memName"));
                    v2.add(rs.getString("b.bookName"));
                    v2.add(rs.getString("l.lendDate"));
                    v2.add(rs.getString("l.lendReturn"));                    
                }
                d.addRow(v2);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(LendBook.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
